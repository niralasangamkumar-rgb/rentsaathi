import requests
import sys
from datetime import datetime

class SimpleAPITester:
    def __init__(self, base_url="http://localhost:8001"):
        self.base_url = base_url
        self.tests_run = 0
        self.tests_passed = 0

    def run_test(self, name, method, endpoint, expected_status):
        """Run a single API test"""
        url = f"{self.base_url}/{endpoint}"
        headers = {'Content-Type': 'application/json'}

        self.tests_run += 1
        print(f"\nğŸ” Testing {name}...")
        
        try:
            if method == 'GET':
                response = requests.get(url, headers=headers, timeout=10)

            success = response.status_code == expected_status
            if success:
                self.tests_passed += 1
                print(f"âœ… Passed - Status: {response.status_code}")
                if response.text:
                    print(f"Response: {response.text[:200]}")
            else:
                print(f"âŒ Failed - Expected {expected_status}, got {response.status_code}")
                print(f"Response: {response.text[:200]}")

            return success, response.json() if success and response.text else {}

        except Exception as e:
            print(f"âŒ Failed - Error: {str(e)}")
            return False, {}

def main():
    # Setup
    tester = SimpleAPITester()
    
    print("ğŸš€ Starting RentSaathi Backend API Tests")
    print("=" * 50)

    # Test basic endpoints
    tester.run_test("Root Endpoint", "GET", "", 200)
    tester.run_test("Health Check", "GET", "api/health", 200)

    # Print results
    print("\n" + "=" * 50)
    print(f"ğŸ“Š Tests passed: {tester.tests_passed}/{tester.tests_run}")
    
    if tester.tests_passed == tester.tests_run:
        print("âœ… All backend tests passed!")
        return 0
    else:
        print("âŒ Some backend tests failed!")
        return 1

if __name__ == "__main__":
    sys.exit(main())